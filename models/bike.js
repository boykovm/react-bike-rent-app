const {Schema, model} = require('mongoose')

const bikeSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    type: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    available: {
        type: Boolean,
        required: true
    }
})

module.exports = model('Bike', bikeSchema)